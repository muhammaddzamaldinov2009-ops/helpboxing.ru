// ========== SHATTER EFFECT ==========
function shatterEffect(event){
  const x = event ? event.clientX : window.innerWidth/2;
  const y = event ? event.clientY : window.innerHeight/2;
  const colors = ['#FF6B35','#F7B801','#00D9A3','#9B59B6','#004E89','#ff4081'];
  for(let i=0;i<18;i++){
    const piece = document.createElement('div');
    piece.className = 'shatter-piece';
    const size = 6+Math.random()*14;
    const angle = (Math.PI*2*i)/18 + Math.random()*0.5;
    const dist = 60+Math.random()*120;
    const tx = Math.cos(angle)*dist;
    const ty = Math.sin(angle)*dist - 40;
    const rot = (Math.random()-0.5)*720;
    const dur = 0.6+Math.random()*0.5;
    piece.style.cssText = `left:${x}px;top:${y}px;width:${size}px;height:${size}px;background:${colors[Math.floor(Math.random()*colors.length)]};--tx:${tx}px;--ty:${ty}px;--rot:${rot}deg;--dur:${dur}s;border-radius:${Math.random()>0.5?'50%':'3px'};`;
    document.body.appendChild(piece);
    setTimeout(()=>piece.remove(),(dur+0.1)*1000);
  }
}

