function calculate(){
  const res = document.getElementById('calcResult');
  const isArc = document.getElementById('modeArcBtn').classList.contains('active');

  if(isArc){
    const x = parseFloat(document.getElementById('arcVal').value);
    const fn = document.getElementById('arcFunc').value;
    if(fn!=='arctg' && (isNaN(x) || x<-1 || x>1)){
      res.innerHTML = `<div class="calc-error">Значение должно быть от −1 до 1!</div>`; return;
    }
    let radVal, deg, piStr;
    if(fn==='arcsin'){ radVal = Math.asin(x); }
    else if(fn==='arccos'){ radVal = Math.acos(x); }
    else { radVal = Math.atan(x); }
    deg = radVal * 180 / Math.PI;
    piStr = toPiStr(radVal);
    res.innerHTML = `
      <div class="calc-result-row"><span class="calc-result-label">${fn}(${x})</span><span class="calc-result-val">${deg.toFixed(4)}°</span></div>
      <div class="calc-result-row"><span class="calc-result-label">В радианах</span><span class="calc-result-val">${radVal.toFixed(6)} рад</span></div>
      <div class="calc-result-row"><span class="calc-result-label">В долях π</span><span class="calc-result-val">${piStr}</span></div>
      <div class="calc-result-row"><span class="calc-result-label">Область значений ${fn}</span><span class="calc-result-val">${fn==='arccos'?'[0°; 180°]':fn==='arcsin'?'[−90°; 90°]':'(−90°; 90°)'}</span></div>`;
  } else {
    const aVal = parseFloat(document.getElementById('trigAngleVal').value);
    const unit = document.getElementById('trigAngleUnit').value;
    if(isNaN(aVal)){ res.innerHTML=`<div class="calc-error">Введите число!</div>`; return; }
    const rad = unit==='deg' ? aVal*Math.PI/180 : aVal;
    const deg = unit==='deg' ? aVal : aVal*180/Math.PI;
    const sinV = Math.sin(rad), cosV = Math.cos(rad);
    const tgV = Math.abs(cosV)<1e-10 ? null : sinV/cosV;
    const ctgV = Math.abs(sinV)<1e-10 ? null : cosV/sinV;
    res.innerHTML = `
      <div class="calc-result-row"><span class="calc-result-label">Угол</span><span class="calc-result-val">${deg.toFixed(4)}° = ${rad.toFixed(6)} рад</span></div>
      <div class="calc-result-row"><span class="calc-result-label">sin(${deg.toFixed(2)}°)</span><span class="calc-result-val">${sinV.toFixed(8)}</span></div>
      <div class="calc-result-row"><span class="calc-result-label">cos(${deg.toFixed(2)}°)</span><span class="calc-result-val">${cosV.toFixed(8)}</span></div>
      <div class="calc-result-row"><span class="calc-result-label">tg(${deg.toFixed(2)}°)</span><span class="calc-result-val">${tgV===null?'не существует':tgV.toFixed(8)}</span></div>
      <div class="calc-result-row"><span class="calc-result-label">ctg(${deg.toFixed(2)}°)</span><span class="calc-result-val">${ctgV===null?'не существует':ctgV.toFixed(8)}</span></div>`;
  }
}

function toPiStr(rad){
  const frac = rad/Math.PI;
  const nice = [[1,1,'π'],[1,2,'π/2'],[1,3,'π/3'],[1,4,'π/4'],[1,6,'π/6'],[2,3,'2π/3'],[3,4,'3π/4'],[5,6,'5π/6'],[0,1,'0'],[-1,2,'−π/2'],[-1,3,'−π/3'],[-1,4,'−π/4'],[-1,6,'−π/6']];
  for(const [n,d,s] of nice){
    if(Math.abs(frac - n/d)<0.0001) return s;
  }
  return (frac.toFixed(4))+'π';
}

